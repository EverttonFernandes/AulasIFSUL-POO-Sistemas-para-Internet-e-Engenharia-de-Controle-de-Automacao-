public class Carro {
    public String cor;
    public String modelo;
    public int anoDeFabricacao;
    public String placa;

    public Carro(String cor, String modelo, int anoDeFabricacao, String placa) {
        this.cor = cor;
        this.modelo = modelo;
        this.anoDeFabricacao = anoDeFabricacao;
        this.placa = placa;
    }
}
